#include <iostream>
using namespace std;

//Pack expansion
template<bool... digits>
int reversed_binary_value()
{
    int sum = 0;
    int product = 1;
    bool binary[] = { digits... };

    for (bool digit : binary)       //goes throegh eacvh digit (0111)
    {
        sum += (digit ? product : 0);   //
        product *= 2;
    }
    return sum;
}



template <int n, bool...digits>
struct CheckValues {
  	static void check(int x, int y)
  	{
    	CheckValues<n-1, 0, digits...>::check(x, y);
    	CheckValues<n-1, 1, digits...>::check(x, y);
  	}
};

template <bool...digits>
struct CheckValues<0, digits...> {
  	static void check(int x, int y)
  	{
    	int z = reversed_binary_value<digits...>();
    	std::cout << (z+64*y==x);
  	}
};

int main()
{
  	int t; std::cin >> t;

  	for (int i=0; i!=t; ++i) {
		int x, y;
    	cin >> x >> y;
    	CheckValues<6>::check(x, y);
    	cout << "\n";
  	}
}
